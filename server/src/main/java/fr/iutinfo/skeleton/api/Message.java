package fr.iutinfo.skeleton.api;

public class Message {

	private String retour;
	private String name;
	
	public Message() {
		
	}

	
	
	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public String getRetour() {
		return retour;
	}

	public void setRetour(String retour) {
		this.retour = retour;
	}
	
	
	
}
